package mdmnw.utils;

import java.util.HashMap;

public class SurvivorshipConfigObj {

	private String objType;
	private String sourceFld;
	private String usageType;
	private String lastVerifiedFld;
	private HashMap<String, Integer> sourcePriorityMap;
	private String[] survivorFlds;
	
	public String getObjType() {
		return objType;
	}
	public void setObjType(String objType) {
		this.objType = objType;
	}
	public String getSourceFld() {
		return sourceFld;
	}
	public void setSourceFld(String sourceFld) {
		this.sourceFld = sourceFld;
	}
	public String getUsageType() {
		return usageType;
	}
	public void setUsageType(String usageType) {
		this.usageType = usageType;
	}
	public String getLastVerifiedFld() {
		return lastVerifiedFld;
	}
	public void setLastVerifiedFld(String lastVerifiedFld) {
		this.lastVerifiedFld = lastVerifiedFld;
	}
	public HashMap<String, Integer> getSourcePriorityMap() {
		return sourcePriorityMap;
	}
	public void setSourcePriorityMap(HashMap<String, Integer> sourcePriorityMap) {
		this.sourcePriorityMap = sourcePriorityMap;
	}
	public String[] getSurvivorFlds() {
		return survivorFlds;
	}
	public void setSurvivorFlds(String[] survivorFlds) {
		this.survivorFlds = survivorFlds;
	}
}
